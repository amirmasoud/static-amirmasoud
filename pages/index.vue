    }
  },
  mounted() {
    const magicGrid = new MagicGrid({
      container: '.magic-grid',
      animate: true,
      gutter: 20,
      items: 1,
      useMin: true,
      maxColumns: 6,
    })

    magicGrid.listen()

    Promise.all(
      Array.from(document.images)
        .filter((img) => !img.complete)
        .map(
          (img) =>
            new Promise((resolve) => {
              img.onload = img.onerror = resolve
            })
        )
    ).then(() => {
      magicGrid.positionItems()
      this.loadingGrid = false
    })
  },
}
